int sprawdzElementy(){
    for (int i = 0; i < 9; ++i)
    {
        if(wskazniki[i].isSelected()&&"".equals(odpowiedzi[i].getText()))
            return 1;
    }
    boolean exists = false;
    for (int i = 1; i < 9; ++i)
    {
        if("".equals(odpowiedzi[i-1].getText()))
            exists = true;
        if (!"".equals(odpowiedzi[i].getText())&& exists)
            return 2;
    }
return 0;
}
void loadQuestion(int n)
{
    clear();
    if (n < 0)
    {
        questionTextField.setText(""+n);
        return;
    }

    if(n > t.test.size()) return;
    questionTextField.setText(t.test.elementAt(n).getQuest());
    for (int i = 0; i < 9; ++i)
    {
        if(i < t.test.elementAt(n).getCount())
        {
            String qs = t.test.elementAt(n).getAns(i);
            odpowiedzi[i].setText(qs);
            wskazniki[i].setEnabled(true);
            odpowiedzi[i].setEnabled(true);
        }
        else if(i == t.test.elementAt(n).getCount())
        {
            odpowiedzi[i].setText("");
            odpowiedzi[i].setEnabled(true);
            wskazniki[i].setEnabled(false);
        }
            else
            {
                odpowiedzi[i].setText("");
                odpowiedzi[i].setEnabled(false);
                wskazniki[i].setEnabled(false);
            }
    }

    if (t.test.elementAt(n).right_ans < 0)
    {
        clear();
    }

    if (t.test.elementAt(n).right_ans > -1)
    {
      wskazniki[t.test.elementAt(n).right_ans].setSelected(true);
    }


    nextButton.setEnabled(true);
    prevButton.setEnabled(true);

    if (n >= t.test.size() -1)
    {
        //nextButton.setText("Dodaj");
        prevButton.setEnabled(true);
    }
    else
    {
        //nextButton.setText("Następne");
        prevButton.setEnabled(true);
    }
    if (n  == 0)
    {
        prevButton.setEnabled(false);
    }
}
void nowePytanie()
{
    clear();

    for (int i = 0; i <9; ++i )
    {
        odpowiedzi[i].setText("");
        odpowiedzi[i].setEnabled(false);
        wskazniki[i].setEnabled(false);
    }
}
