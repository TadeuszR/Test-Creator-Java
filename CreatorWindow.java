    private void prevButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prevButtonActionPerformed

        if(sprawdzElementy()!=0)
    {
          JOptionPane.showMessageDialog(this,"Popraw dane.","Uwaga", JOptionPane.WARNING_MESSAGE);
        return;
    }
    pytanie = questionTextField.getText();
    for (int i = 0; i < 9; ++i)
    {
        if (wskazniki[i].isSelected())
            prawidlowa = i;
    }
    ilosc = 9;
    for (int i = 0; i < 9; ++i)
    {
        if(!"".equals(odpowiedzi[i].getText()))
            odp[i] = odpowiedzi[i].getText();
        else
        {
            ilosc = i;
            break;
        }
    }
    if (ilosc == 0)
    {
        --iter;
        loadQuestion(iter);
        return;
    }
    int i;
    for (i = 0; i < 9; ++i)
    {
        if(wskazniki[i].isSelected())
        {
            prawidlowa = i;
            break;
        }
    }
    if (i == 9)
    {
          JOptionPane.showMessageDialog(this,"Zaznacz prawidłową odpowiedź","Uwaga", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if(sprawdzElementy()!=0)
    {
          JOptionPane.showMessageDialog(this,"Popraw dane.","Uwaga", JOptionPane.WARNING_MESSAGE);
        return;
    }
    Question q = new Question();
    q.setQuest(pytanie);
    q.right_ans=prawidlowa;
    q.count=ilosc;
    for (int j = 0; j < ilosc; ++j)
        q.ans[j]=odp[j];
    if (iter >= t.test.size())
        t.test.addElement(q);
    else
        t.test.setElementAt(q, iter);
    --iter;
    loadQuestion(iter);
    if (iter  == 0)
    {
        prevButton.setEnabled(false);
    }
    if (t.test.elementAt(iter).right_ans > -1)
    {
      wskazniki[t.test.elementAt(iter).right_ans].setSelected(true);
    }

    }//GEN-LAST:event_prevButtonActionPerformed

